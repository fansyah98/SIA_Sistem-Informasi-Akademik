    private void btn_simpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_simpanMouseClicked
        
        String jk = null ;
        
        if(Rlaki_laki.isSelected()){
            jk = "Laki-Laki";
        }else if(Rperempuan.isSelected()){
         jk = "Perempuan" ;
        }
                
        
        try{
             Statement statement = (Statement)koneksi.getKoneksi().createStatement();
             statement.executeUpdate("INSERT INTO tb_dosen(nid_dosen, nama_dosen, jk_dosen, alamat_dosen) VALUES ('"+txt_nidn.getText()+"','"+txt_nama_dos.getText()+"','"+jk+"','"+txt_alamat.getText()+"')"); 
             statement.close();
             JOptionPane.showMessageDialog(null, "Data Berhasil disimpan !!!");
             clear();
        }
        catch(Exception e){
               JOptionPane.showMessageDialog(rootPane, "Gagal Input data" + e.getMessage());

        }

    }//GEN-LAST:event_btn_simpanMouseClicked

