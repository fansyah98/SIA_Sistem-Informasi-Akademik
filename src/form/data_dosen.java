    private void btn_loadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_loadMouseClicked
        try{
             Statement statement = (Statement)koneksi.getKoneksi().createStatement();
             statement.executeQuery("SELECT * FROM tb_dosen WHERE nid_dosen = '"+txt_nidn.getText()+"' "); 
             statement.close();
             JOptionPane.showMessageDialog(null, "Data Berhasil di Relod");
             clear();

        }catch(Exception e){
              JOptionPane.showMessageDialog(null, "Sialakan reload ulang");
        }
        loadData();
    }//GEN-LAST:event_btn_loadMouseClicked

